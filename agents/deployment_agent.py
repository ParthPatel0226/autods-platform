"""Deployment Agent.

Generates a self-contained FastAPI microservice package from the trained
best model:
  - api.py           FastAPI app with /health, /predict, /predict/batch
  - schemas.py       Pydantic request / response models
  - requirements.txt Minimal production dependencies
  - Dockerfile       Multi-stage Python 3.11 build
  - README.md        Quick-start instructions

All generated text is stored in state["deployment_artifacts"] and
written to outputs/{session_id}/deployment/.
"""

from __future__ import annotations

import json
