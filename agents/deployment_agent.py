logger = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# Type-inference helper
# ---------------------------------------------------------------------------
_DTYPE_TO_PYTHON: dict[str, str] = {
    "int64": "int", "int32": "int", "int16": "int", "int8": "int",
    "float64": "float", "float32": "float",
    "bool": "bool",
    "object": "str", "string": "str",
    "category": "str",
}


def _python_type(dtype: str) -> str:
    return _DTYPE_TO_PYTHON.get(dtype, "Any")


def _safe_get(state: AutoDSState, *keys: str, default: Any = None) -> Any:
    for key in keys:
        val = state.get(key)  # type: ignore[arg-type]
        if val is not None:
            return val
    return default


# ---------------------------------------------------------------------------
# api.py generator
# ---------------------------------------------------------------------------
def _generate_api_py(
    features: list[str],
    target: str,
    problem_type: str,
    model_path: str,
    model_name: str,
) -> str:
    """Generate FastAPI application source code.

    Args:
        features: List of input feature names.
        target: Target column name.
        problem_type: classification or regression.
        model_path: Path to the saved model artifact.
        model_name: Display name of the best model.

    Returns:
        Python source code string for api.py.
    """
    is_clf = problem_type == "classification"
    proba_block = (
        "    if hasattr(model, 'predict_proba'):\n"
        "        proba = float(model.predict_proba(df)[0].max())\n"
        "    else:\n"
        "        proba = None\n"
    ) if is_clf else "    proba = None\n"

    generated_at = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    return f'''"""AutoDS-generated FastAPI prediction service.

Model: {model_name}
Target: {target}
Problem type: {problem_type}
Generated: {generated_at}
"""

from __future__ import annotations

import io
from pathlib import Path
from typing import Any

import joblib
import pandas as pd
from fastapi import FastAPI, File, HTTPException, UploadFile
from fastapi.responses import JSONResponse

from schemas import BatchPredictionResponse, PredictionRequest, PredictionResponse

MODEL_PATH = Path(r"{model_path}")
FEATURES = {json.dumps(features)}

app = FastAPI(
    title="AutoDS Prediction API",
    description="Generated by AutoDS — Autonomous Data Science Platform",
    version="1.0.0",
)

# Load model once at startup
# SECURITY: Uses joblib instead of pickle to reduce deserialization risks.
# Never load model files from untrusted sources.
_model = None
try:
    _loaded = joblib.load(MODEL_PATH)
    _model = _loaded["model"] if isinstance(_loaded, dict) else _loaded
except FileNotFoundError:
    pass  # Will raise 503 on first request


def _get_model() -> Any:
    if _model is None:
        raise HTTPException(status_code=503, detail="Model not loaded. Check MODEL_PATH.")
    return _model


@app.get("/health", tags=["ops"])
def health() -> dict:
    """Liveness probe."""
    return {{"status": "ok", "model": "{model_name}", "target": "{target}"}}


@app.post("/predict", response_model=PredictionResponse, tags=["inference"])
def predict(request: PredictionRequest) -> PredictionResponse:
    """Return a single prediction from a JSON payload."""
    model = _get_model()
    row = {{k: v for k, v in request.model_dump().items() if k in FEATURES}}
    df = pd.DataFrame([row])
    prediction = model.predict(df)[0]
{proba_block}
    return PredictionResponse(
        prediction=prediction,
        probability=proba,
        model_name="{model_name}",
    )


@app.post("/predict/batch", response_model=BatchPredictionResponse, tags=["inference"])
async def predict_batch(file: UploadFile = File(...)) -> BatchPredictionResponse:
    """Return predictions for a CSV file upload."""
    model = _get_model()
    contents = await file.read()
    df = pd.read_csv(io.BytesIO(contents))
    missing = [c for c in FEATURES if c not in df.columns]
    if missing:
        raise HTTPException(
            status_code=422,
            detail=f"Missing required columns: {{missing}}",
        )
    preds = model.predict(df[FEATURES]).tolist()
    probas: list = [None] * len(preds)
    if hasattr(model, "predict_proba"):
        probas = model.predict_proba(df[FEATURES]).max(axis=1).tolist()
    results = [
        {{"prediction": p, "probability": pr, "model_name": "{model_name}"}}
        for p, pr in zip(preds, probas)
    ]
    return BatchPredictionResponse(predictions=results, count=len(results))
'''


# ---------------------------------------------------------------------------
# schemas.py generator
# ---------------------------------------------------------------------------
def _generate_schemas_py(
    features: list[str],
    schema_info: dict,
    problem_type: str,
) -> str:
    """Generate Pydantic schema source code for request/response models.

    Args:
        features: List of feature names.
        schema_info: Schema metadata with column dtype info.
        problem_type: classification or regression.

    Returns:
        Python source code string for schemas.py.
    """
    columns_meta: list[dict] = schema_info.get("columns", [])
    dtype_map: dict[str, str] = {
        col.get("name", ""): col.get("dtype", "object")
        for col in columns_meta
        if col.get("name")
    }

    if features:
        field_lines = "\n    ".join(
            f'{f}: {_python_type(dtype_map.get(f, "object"))} = ...'
            for f in features
        )
    else:
        field_lines = "pass"

    proba_field = "    probability: float | None = None\n" if problem_type == "classification" else ""
    generated_at = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    return f'''"""Pydantic schemas for the AutoDS prediction API.

Generated: {generated_at}
"""

from __future__ import annotations

from datetime import datetime, timezone
from typing import Any

from pydantic import BaseModel, Field


class PredictionRequest(BaseModel):
    """One row of feature values for a single prediction."""
    {field_lines}


class PredictionResponse(BaseModel):
    """Prediction result for a single request."""
    prediction: Any
{proba_field}    model_name: str
    timestamp: str = Field(
        default_factory=lambda: datetime.now(timezone.utc).isoformat()
    )


class BatchPredictionResponse(BaseModel):
    """Prediction results for a CSV batch request."""
    predictions: list[dict[str, Any]]
    count: int
    timestamp: str = Field(
        default_factory=lambda: datetime.now(timezone.utc).isoformat()
    )
'''


