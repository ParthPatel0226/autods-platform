# ---------------------------------------------------------------------------
# Public entry point
# ---------------------------------------------------------------------------
def generate_deployment(state: AutoDSState) -> AutoDSState:
    """Generate FastAPI deployment package and update state.

    Produces: api.py, schemas.py, requirements.txt, Dockerfile, README.md
    Writes to: outputs/{session_id}/deployment/

    Args:
        state: Current pipeline state.

    Returns:
        Updated state with deployment_artifacts, deployment_path, api_endpoint_url.
    """
    state["current_step"] = "deployment"
    state["completed_steps"] = state.get("completed_steps", []) + ["deployment"]

    session_id = state.get("session_id", "session_unknown")
    best_model = _safe_get(state, "best_model", "best_model_name", default="best_model")
    model_path = state.get("best_model_path", "model.pkl")
    features: list[str] = _safe_get(state, "feature_list", "features_selected", default=[])
    target = state.get("target_column", "target")
    problem_type = state.get("problem_type", "classification")
    schema_info = state.get("schema_info") or {}

    out_dir = Path("outputs") / session_id / "deployment"
    out_dir.mkdir(parents=True, exist_ok=True)

    artifacts: dict[str, str] = {}

    generators = [
        ("api.py", lambda: _generate_api_py(features, target, problem_type, model_path, best_model)),
        ("schemas.py", lambda: _generate_schemas_py(features, schema_info, problem_type)),
        ("requirements.txt", _generate_requirements),
        ("Dockerfile", lambda: _generate_dockerfile(model_path)),
        ("README.md", lambda: _generate_readme(best_model, features, target)),
    ]

    for filename, gen_fn in generators:
        try:
            artifacts[filename] = gen_fn()
        except Exception as exc:
            logger.error("%s generation failed: %s", filename, exc, exc_info=True)
            state["errors"] = state.get("errors", []) + [{
                "step": "deployment",
                "type": f"{filename}_failed",
                "detail": str(exc),
            }]

    # Write all successfully generated artifacts to disk
    for filename, content in artifacts.items():
        try:
            (out_dir / filename).write_text(content, encoding="utf-8")
        except Exception as exc:
            logger.error("Failed to write %s: %s", filename, exc, exc_info=True)
            state["errors"] = state.get("errors", []) + [{
                "step": "deployment",
                "type": "write_failed",
                "detail": f"{filename}: {exc}",
            }]

    state["deployment_artifacts"] = artifacts
    state["deployment_path"] = str(out_dir)
    state["api_endpoint_url"] = "http://localhost:8000"
    logger.info(
        "Deployment artifacts generated: %s -> %s",
        list(artifacts.keys()), out_dir,
    )
    return state
