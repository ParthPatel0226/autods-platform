# Dockerfile generator
# ---------------------------------------------------------------------------
def _generate_dockerfile(model_path: str) -> str:
    """Generate multi-stage Dockerfile for the prediction API.

    Args:
        model_path: Path to the saved model artifact.

    Returns:
        Dockerfile content string.
    """
    model_filename = Path(model_path).name
    return f"""# Multi-stage build — AutoDS generated Dockerfile
FROM python:3.11-slim AS builder
WORKDIR /app
COPY requirements.txt .
RUN pip install --no-cache-dir --upgrade pip \\
    && pip install --no-cache-dir -r requirements.txt

FROM python:3.11-slim AS runtime
WORKDIR /app
COPY --from=builder /usr/local/lib/python3.11/site-packages /usr/local/lib/python3.11/site-packages
COPY --from=builder /usr/local/bin /usr/local/bin
COPY api.py schemas.py {model_filename} ./
EXPOSE 8000
CMD ["uvicorn", "api:app", "--host", "0.0.0.0", "--port", "8000"]
"""


